        LinkedList<Integer> parValor(int row, int col) {
            LinkedList<Integer> row_Col = new LinkedList<>();
            row_Col.add(row);
            row_Col.add(col);
            return row_Col;
        }

        LinkedList<Integer> bfs(int sRow, int sCol) {
            LinkedList< LinkedList<Integer>> q_Row_Col = new LinkedList<>();
            LinkedList<Integer> caminho = new LinkedList<>();
            q_Row_Col.add(parValor(sRow, sCol));
            matVisitado[sRow].set(sCol);
            caminho.add(mat[sRow][sCol]);

            while (!q_Row_Col.isEmpty()) {
                LinkedList<Integer> v_row_col = q_Row_Col.pop();
                int vRow = v_row_col.getFirst();
                int vCol = v_row_col.getLast();

                for (int i = 0; i < 4; i++) {
                    int vRow_Visitado = vRow + row_PosY[i];
                    int vCol_Visitado = vCol + col_PosX[i];

                    if (vRow_Visitado >= 0 && vCol_Visitado >= 0 && vRow_Visitado < maxRow && vCol_Visitado < maxCol) {
                        if (matVisitado[vRow_Visitado].get(vCol_Visitado) == false && mat[vRow_Visitado][vCol_Visitado] > 0) {

                            q_Row_Col.add(parValor(vRow_Visitado, vCol_Visitado));
                            caminho.add(mat[vRow_Visitado][vCol_Visitado]);
                            matVisitado[vRow_Visitado].set(vCol_Visitado);

                        }
                    }
                }

            }

            return caminho;
        }

